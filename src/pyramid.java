
public class pyramid {

	public static void main(String[] args) {
	
		
		int p=1;
		for(int i=1; i<5; i++) {  // i=1
			
			for(int j=1; j<=i; j++){	//j=1
				
				
				System.out.print(p);  // print - to print in single line..println()..print and go to new line		
				System.out.print("\t");
				p++;
				
				
			}
				System.out.println("");
				
				
			}			
			
	}
}





