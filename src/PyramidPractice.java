
public class PyramidPractice {

	public static void main(String[] args) {
	
		
	int p=3;
		for(int i=1; i<4; i++) {  // i=1
			
			for(int j=3; j<=i+2; j++){	//j=1
				
				
				System.out.print(p);  // print - to print in single line..println()..print and go to new line		
				System.out.print("\t");
				
				p=p+3;
				
				
			}
				System.out.println("");
				
				
			}			
			
	}
}





